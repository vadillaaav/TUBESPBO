    private void btnMasukActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMasukActionPerformed
        
        try{
            String sql = "SELECT * FROM mahasiswa WHERE nim = '"+txtnim.getText()+"' AND nama = '"+txtnama.getText()+"'";
            java.sql.Connection conn = (Connection)Config.configDB();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            java.sql.ResultSet rs = pst.executeQuery(sql);
            if(rs.next()){
                if(txtnim.getText().equals(rs.getString("nim"))&&txtnama.getText().equals(rs.getString("nama"))){
                    JOptionPane.showMessageDialog(null, "Berhasil login sebagai Mahasiswa");
                    this.setVisible(false);
                    new Mahasiswa().setVisible(true);
                    
                }else {
                    JOptionPane.showMessageDialog(this, "Gagal login");
                }
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Gagal login");
        }
    }//GEN-LAST:event_btnMasukActionPerformed

