    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            String query = "SELECT * FROM `multiuserlogin` WHERE username=? and password =? and usertype=?";
            con = DriverManager.getConnection("jdbc:mysql://localhost/multiuserlogin", "root", "");
            pst = con.prepareStatement(query);
            pst.setString(1, jTextField1.getText());
            pst.setString(2, jPasswordField1.getText());
            pst.setString(3, String.valueOf(jComboBox1.getSelectedItem()));
            rs = pst.executeQuery();
            if(rs.next()) {
                JOptionPane.showMessageDialog(this, "kamu berhasil login sebagai " + rs.getString("usertype"));
            if(jComboBox1.getSelectedIndex()==0){
                admin a = new admin();
                a.setVisible(true);
                this.setVisible(false);
            } else if (jComboBox1.getSelectedIndex()==1) {
                dosen d = new dosen();
                d.setVisible(true);
                this.setVisible(false);
            } else {
                mahasiswa m = new mahasiswa();
                m.setVisible(true);
                this.setVisible(false);
            }
            } else {
                JOptionPane.showMessageDialog(this, "username dan password salah");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

